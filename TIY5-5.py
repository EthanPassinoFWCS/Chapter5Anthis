def theTest(alc):
  alien_color = alc
  if alien_color == "green":
    print("Player has earned 5 points!")

  elif alien_color == "yellow":
    print("Player has earned 10 points!")

  elif alien_color == "red":
    print("Player has earned 15 points!")

theTest("green")
theTest("yellow")
theTest("red")